class Employee {
    constructor(type, name, color) {
      this.type = type;
      this.name = name;
      this.color = color;
    }
    eat(food) {
      console.log(`The ${this.type} ${this.name} is eating some ${food}`);
    }
    sleep() {
      console.log("Zzz");
    }
    walk() {
      console.log(`The ${this.type} ${this.name} is walking`);
    }
  }
  
